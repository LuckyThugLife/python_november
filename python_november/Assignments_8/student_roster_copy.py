# source list
student_roster = ["Emma", "Liam", "Olivia", "Noah", "Ava"]

# changing the order of the elements in the list in the 
# opposite direction using the reverse method
student_roster.reverse()
print(student_roster)

# sort the resulting list in alphabetical order using 
# the sort method without specifying additional attributes
student_roster.sort()
print(student_roster)
