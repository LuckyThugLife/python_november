# Запрашиваем у пользователя типы данных

i = int(input("Введите целое число: "))
f = float(input("Введите дробное число: "))
s = input("Введите строку: ")
b = input("Введите булево значение (True или False): ")

# Выводим типы данных
print(f"Целое число: {i}")
print(f"Число с плавающей точкой: {f}")
print(f"Строка: {s}")
print(f"Булево значение: {b}")
