# Операторы if-elif-else в Python  позволяют  написать код,
# который выполняет одно действие, если условие истинно, и другое действие, если условие ложно.
# например:

x = 10

if x > 10:
    print("x больше 10")
elif x == 10:
    print("x равен 10")
else:
    print("x меньше 10")
